"""Render CutLang yield/cutflow CSVs into PDF tables.

Inputs (generated by Stack_plot.py in this folder):
  - cutflow_table.csv
  - final_yields_summary.csv
  - hist_integrals_summary.csv (optional for a 1-page summary)

Outputs:
  - cutflow_tables.pdf (one page per region)
  - final_yields_summary.pdf (one page)
  - hist_integrals_summary.pdf (one page)
"""

from __future__ import annotations

import argparse
import csv
from collections import defaultdict
from pathlib import Path
from typing import Any

import matplotlib

matplotlib.use("Agg")
import matplotlib.pyplot as plt
from matplotlib.backends.backend_pdf import PdfPages


def _read_csv(path: Path) -> list[dict[str, str]]:
	with path.open("r", newline="") as f:
		reader = csv.DictReader(f)
		return list(reader)


def _to_float(value: str | None) -> float | None:
	if value is None:
		return None
	value = value.strip()
	if value == "":
		return None
	try:
		return float(value)
	except ValueError:
		return None


def _fmt_val_err(v: str | None, e: str | None) -> str:
	vv = _to_float(v)
	ee = _to_float(e)
	if vv is None:
		return ""
	if ee is None:
		return f"{vv:.3g}"
	return f"{vv:.3g} ± {ee:.2g}"


def _make_table_page(
	*,
	title: str,
	columns: list[str],
	rows: list[list[str]],
	out_pdf: PdfPages,
	font_size: int = 8,
	col_widths: list[float] | None = None,
	figsize=(11.69, 8.27),
):
	fig, ax = plt.subplots(figsize=figsize)  # A4 landscape in inches-ish
	ax.axis("off")
	ax.set_title(title, fontsize=12, pad=12)

	tbl = ax.table(
		cellText=rows,
		colLabels=columns,
		cellLoc="left",
		colLoc="left",
		loc="center",
		colWidths=col_widths,
	)
	tbl.auto_set_font_size(False)
	tbl.set_fontsize(font_size)
	tbl.scale(1.0, 1.2)

	# Light header styling.
	for (r, c), cell in tbl.get_celld().items():
		cell.set_linewidth(0.25)
		if r == 0:
			cell.set_text_props(weight="bold")
			cell.set_facecolor("#f0f0f0")

	fig.tight_layout()
	out_pdf.savefig(fig)
	plt.close(fig)


def render_cutflow_tables(*, cutflow_csv: Path, out_pdf: Path):
	rows = _read_csv(cutflow_csv)
	by_region: dict[str, list[dict[str, str]]] = defaultdict(list)
	for r in rows:
		by_region[r["region"]].append(r)

	with PdfPages(out_pdf) as pdf:
		for region in sorted(by_region.keys()):
			region_rows = sorted(by_region[region], key=lambda x: int(x.get("bin", "0") or 0))

			has_data = any((rr.get("data") or "").strip() for rr in region_rows)

			columns = ["Cut"]
			if has_data:
				columns.append("DATA")
			columns.extend(["ttbar", "signal"])

			table_rows: list[list[str]] = []
			for rr in region_rows:
				cut = rr.get("cut", "")
				if not cut:
					continue

				row_cells = [cut]
				if has_data:
					row_cells.append(_fmt_val_err(rr.get("data"), rr.get("data_err")))
				row_cells.append(_fmt_val_err(rr.get("ttbar"), rr.get("ttbar_err")))
				row_cells.append(_fmt_val_err(rr.get("signal"), rr.get("signal_err")))
				table_rows.append(row_cells)

			# Make the figure height depend on number of rows (up to a sensible max).
			base_h = 2.5
			per_row = 0.22
			height = min(20.0, base_h + per_row * max(10, len(table_rows)))
			figsize = (11.69, height)

			_make_table_page(
				title=f"Cutflow Table — {region}",
				columns=columns,
				rows=table_rows,
				out_pdf=pdf,
				font_size=8,
				col_widths=[0.55] + ([0.15] if has_data else []) + [0.15, 0.15],
				figsize=figsize,
			)


def render_final_yields(*, final_csv: Path, out_pdf: Path):
	rows = _read_csv(final_csv)
	rows = sorted(rows, key=lambda r: r.get("region", ""))
	has_data = any((r.get("data") or "").strip() for r in rows)

	columns = ["Region", "Final cut"]
	if has_data:
		columns.append("DATA")
	columns.extend(["ttbar", "signal"])

	table_rows: list[list[str]] = []
	for r in rows:
		region = r.get("region", "")
		final_cut = r.get("final_cut", "")
		row = [region, final_cut]
		if has_data:
			row.append(_fmt_val_err(r.get("data"), r.get("data_err")))
		row.append(_fmt_val_err(r.get("ttbar"), r.get("ttbar_err")))
		row.append(_fmt_val_err(r.get("signal"), r.get("signal_err")))
		table_rows.append(row)

	with PdfPages(out_pdf) as pdf:
		_make_table_page(
			title="Final Yields Summary",
			columns=columns,
			rows=table_rows,
			out_pdf=pdf,
			font_size=9,
			col_widths=[0.12, 0.48] + ([0.13] if has_data else []) + [0.13, 0.14],
			figsize=(11.69, 8.27),
		)


def render_hist_integrals(*, integrals_csv: Path, out_pdf: Path):
	if not integrals_csv.exists():
		return
	rows = _read_csv(integrals_csv)

	columns = ["Region", "Hist", "Sample", "Integral"]
	table_rows: list[list[str]] = []
	for r in sorted(rows, key=lambda x: (x.get("region", ""), x.get("hist", ""), x.get("sample", ""))):
		table_rows.append(
			[
				r.get("region", ""),
				r.get("hist", ""),
				r.get("sample", ""),
				_fmt_val_err(r.get("value"), r.get("error")),
			]
		)

	with PdfPages(out_pdf) as pdf:
		_make_table_page(
			title="Histogram Integrals (Integral ± Error)",
			columns=columns,
			rows=table_rows,
			out_pdf=pdf,
			font_size=9,
			col_widths=[0.15, 0.10, 0.15, 0.60],
			figsize=(11.69, 8.27),
		)


def main(argv: list[str] | None = None) -> int:
	ap = argparse.ArgumentParser()
	ap.add_argument("--cutflow", default="cutflow_table.csv")
	ap.add_argument("--final", default="final_yields_summary.csv")
	ap.add_argument("--integrals", default="hist_integrals_summary.csv")
	ap.add_argument("--out-cutflow-pdf", default="cutflow_tables.pdf")
	ap.add_argument("--out-final-pdf", default="final_yields_summary.pdf")
	ap.add_argument("--out-integrals-pdf", default="hist_integrals_summary.pdf")
	args = ap.parse_args(argv)

	cutflow_csv = Path(args.cutflow)
	final_csv = Path(args.final)
	integrals_csv = Path(args.integrals)

	if not cutflow_csv.exists():
		raise SystemExit(f"Missing input: {cutflow_csv}")
	if not final_csv.exists():
		raise SystemExit(f"Missing input: {final_csv}")

	render_cutflow_tables(cutflow_csv=cutflow_csv, out_pdf=Path(args.out_cutflow_pdf))
	render_final_yields(final_csv=final_csv, out_pdf=Path(args.out_final_pdf))
	render_hist_integrals(integrals_csv=integrals_csv, out_pdf=Path(args.out_integrals_pdf))
	return 0


if __name__ == "__main__":
	raise SystemExit(main())

